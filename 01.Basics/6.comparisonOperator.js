//Comparison Operators

//==> Relational Operators
//==, !=, >, <, >=, <=

//==> Equality Operator
//==, !=

//==> Strict Equality Operator
//===, !== (checks type as well as value)

console.log(10 == "10"); //true
console.log(10 != "10"); //false
console.log(10 === "10"); //false
console.log(10 !== "10"); //true
//================================//
console.log(10 > 9); //true
console.log(10 < 9); //false
console.log(10 >= 9); //true
console.log(10 <= 9); //false


