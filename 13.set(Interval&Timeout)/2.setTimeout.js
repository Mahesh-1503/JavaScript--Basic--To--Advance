/**
 * setTimeout():
 * - The setTimeout() method is used to delay the execution of a function for a specified amount of time.
 * - The setTimeout() method returns a timer ID, which can later be used to clear the timeout.
 * - The setTimeout() method is used to execute a function after a specified delay.
 */

setTimeout(() => {
    console.log('Hello world');
}, 2000); // This will print 'Hello world' after 1 second